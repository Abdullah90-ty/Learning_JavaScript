// For example:-
 let a = 5;
 let b = 6;
 console.log(a+b);    // return 11 
 console.log(a-b);    // return -1
 console.log(a*b);    // return 30 
 console.log(a/b);    // return 0.33 
 console.log(a%b);    // return 2
 console.log(a**b);   // return 15,625
 

