/*
<---------concat function---------.
It is used in array to concatenate an array with other.
It's syntax as follows:-
     array.concat(array);
1. first array is our orignal array.
2. second array is an array which we want to merge.
*/
// Program:-
let array1 = ["1", "2", "3"];
let array2 = ["6", "5", "4"];
let array3 = array1.concat(array2); 
console.log(array3);   // result ['1', '2', '3', '6', '5', '4']
